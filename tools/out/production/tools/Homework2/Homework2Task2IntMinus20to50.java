/* Да се изведат на екрана числат от -20 до 50.
*/

package Homework2;

public class Homework2Task2IntMinus20to50 {
    public static void main(String[] args) {
        System.out.println("Start Homework2task2");
        int num = -20;
        while (num <= 50) {
            System.out.println(num);
            num++;
        }
        System.out.println("\n" + "End Homework2task2");
    }
}
