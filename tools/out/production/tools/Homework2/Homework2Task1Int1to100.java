/* Да се изведат на екрана числата от 1 до 100.
*/
package Homework2;

public class Homework2Task1Int1to100 {
    public static void main(String[] args) {
        System.out.println("Start Homework2task1");
        int num = 1;
        while (num <= 100) {
            System.out.println(num);
            num++;
        }
        System.out.println("\n" + "End Homework2task1");
    }
}
