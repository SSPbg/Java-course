/* Да се изведат на екрана числата от 10 до 1 в обратен ред.
*/

package Homework2;

public class Homework2Task4From10to1 {

    public static void main(String[] args) {
        System.out.println("Start Homework2task4");
        for (int i = 10; i >= 1; i--) {
            System.out.print(i + " ");
        }
        System.out.println("\n" + "End Homework2task5");
    }
}